#Check An Inputed Number Is Even Or Odd
number = int(input ("Enter A Number For Check That A Even Or Odd Number: ")) #User Input
#Check The Number Is Even Or Odd
if number%2==0:
    print('The Inputed Number Is A Even Number') #Print If The Number Is Even
else:
    print('The Inputed Number Is an Odd Number') #print If The Number Is Odd